<template>
  <div class="tab-control">
    <div v-for="(item,index) in titles" :key="index"
         @click="itemClick(index)"
         class="tab-control-item"
        :class="{active: index===currentIndex}">
      <span>{{item}}</span>
    </div>
  </div>
</template>

<script>
  export default {
    name: "TabControl",
    props:{
      titles:{
        type: Array,
        default(){
          return []
        }
      }
    },
    data(){
      return{
        currentIndex: 0
      }
    },
    methods:{
      itemClick(index){
        this.currentIndex=index;
        this.$emit('tabClick',index)
      }
    }
  }
</script>

<style scoped>
  .tab-control{
    display: flex;
    text-align: center;
    font-size: 15px;
    heigth: 40px;
    line-height: 40px;
    background-color: #ffffff;
  }
  .tab-control-item{
    flex:1;
  }
  .tab-control-item span{
    padding: 5px;  /*设置横线和文字的间隔*/
  }
  .active{
    color: var(--color-high-text); /*动态绑定文字颜色*/
  }
  .active span{
    border-bottom: 3px solid var(--color-tint); /*定义span的下边框*/
  }
</style>
